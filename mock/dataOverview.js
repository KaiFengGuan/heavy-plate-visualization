function generateThickCurveData() {
  let position = [];
  let left = [];
  let right = [];
  let center = [];

  let count = 0;
  for (let i = 0; i < 100; i++) {
    position.push(count);
    count += 0.4;
    left.push(Math.random() * 10 + 35);
    right.push(Math.random() * 10 + 35);
    center.push(Math.random() * 10 + 35);
  }
  return {
    position,
    left,
    right,
    center
  }
}

